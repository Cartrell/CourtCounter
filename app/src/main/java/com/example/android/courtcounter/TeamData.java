package com.example.android.courtcounter;

class TeamData {
  /////////////////////////////////////////////////////////////////////////////
  //===========================================================================
  // member variables
  //===========================================================================
  /////////////////////////////////////////////////////////////////////////////
  private int m_runs;
  private int m_hits;

  /////////////////////////////////////////////////////////////////////////////
  //===========================================================================
  // ctor
  //===========================================================================
  /////////////////////////////////////////////////////////////////////////////
  TeamData() {
  }

  /////////////////////////////////////////////////////////////////////////////
  //===========================================================================
  // package private
  //===========================================================================
  /////////////////////////////////////////////////////////////////////////////

  //===========================================================================
  // getHits
  //===========================================================================
  int getHits() {
    return(m_hits);
  }

  //===========================================================================
  // getRuns
  //===========================================================================
  int getRuns() {
    return(m_runs);
  }

  //===========================================================================
  // setHits
  //===========================================================================
  void setHits(int value) {
    m_hits = Math.max(value, 0);
  }

  //===========================================================================
  // setRuns
  //===========================================================================
  void setRuns(int value) {
    m_runs = Math.max(value, 0);
  }
}